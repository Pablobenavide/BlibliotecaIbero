    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String idString = txtIdLibro.getText();
        String nombre = txtNombr.getText();
        String autor = txtAutor.getText();
        String descripcion = txtDescrip.getText();
        int id;

        try {
            id = Integer.parseInt(idString);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID del libro debe ser un n√∫mero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Libro libro = Arboles.Arboles.libros.buscarLibroPorID(id);

        if (libro != null) {
            libro.setNombre(nombre);
            libro.setAutor(autor);
            libro.setDescripcion(descripcion);
            JOptionPane.showInternalMessageDialog(null, "Libro editado :)");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ ning√∫n libro con el ID especificado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        txtIdLibro.setText("");
        txtNombr.setText("");
        txtAutor.setText("");
        txtDescrip.setText("");
        txtIdLibro.requestFocus();
        txtNombr.requestFocus();
        txtAutor.requestFocus();
        txtDescrip.requestFocus();


    }//GEN-LAST:event_jButton2ActionPerformed

